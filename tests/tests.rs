fn nested_nested_groups_1() {
    let input = "Recv | 8=FIX.4.4 | 555=2 | 604=2 | 605=F7 | 605=CGYU0 | 604=2 | 605=F7 | 605=CGYM0 | 10=209";
    let output = r#"{"8":"FIX.4.4","555":[{"604":[{"605":"F7"},{"605":"CGYU0"}]},{"604":[{"605":"F7"},{"605":"CGYM0"}]}],"10":"209"}"#;

    if let Some(fix_message) = fixparser::FixMessage::from_tag_value(&input) {
        assert_eq!(output, fix_message.to_json().to_string());
    } else {
        panic!("It's not a FIX-compliant message");
    }
}

